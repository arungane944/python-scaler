# Datatypes in python
#    1. integer
#    2. float
#    3. booleans
#    4. Strings
#    5. None


# Integer
age = 25
print(type(age))  # Output: <class 'int'>

# Float
price = 99.99
print(type(price))  # Output: <class 'float'>

# Boolean
is_raining = True
print(type(is_raining))  # Output: <class 'bool'>

# String
name = "John"
print(type(name))  # Output: <class 'str'>

# None
result = None
print(type(result))  # Output: <class 'NoneType'>
